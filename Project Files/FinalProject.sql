DROP TABLE IF EXISTS PnRTable;
CREATE TABLE PnRTable (
	GEOID INTEGER,
	County VARCHAR(50),
	State VARCHAR(2),
	Year INTEGER,
	PnR VARCHAR(50),
	PRIMARY KEY(GEOID, Year)
);

INSERT INTO PnRTable
SELECT DISTINCT PrEPData.GEOID, PrEPData.County, PrEPData.State, PrEPData.Year, 1.0*PrEPData.CountyPrEPUsers/HIVNewIncidencesNJ.HIVNEWINCIDENCES AS PnR
FROM PrEPData
			INNER JOIN HIVNewIncidencesNJ
			ON HIVNewIncidencesNJ.GEOID = PrEPData.GEOID AND HIVNewIncidencesNJ.Year = PrEPData.Year
			;

